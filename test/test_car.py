import unittest
from datetime import datetime
from engine.capulet_engine import CapuletEngine
from engine.spindler_battery import SpindlerBattery

class TestCapuletEngine(unittest.TestCase):
    def test_needs_service(self):
        # Create a CapuletEngine instance with last service date and mileage
        engine = CapuletEngine(last_service_date=datetime(2022, 1, 1), current_mileage=25000, last_service_mileage=20000)
        
        # Test if the engine needs service (should return True)
        self.assertTrue(engine.needs_service())

        # Test if the engine does not need service (should return False)
        engine = CapuletEngine(last_service_date=datetime(2022, 1, 1), current_mileage=22000, last_service_mileage=20000)
        self.assertFalse(engine.needs_service())

